package Math;

public class Point3 {
    private int x, y, w;

    // constructor
    public Point3(int x, int y, int w) {
        this.x = x;
        this.y = y;
        this.w = w;
    }

    // getters
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getW() {
        return w;
    }

    // setters
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setW(int w) {
        this.w = w;
    }

    
}
